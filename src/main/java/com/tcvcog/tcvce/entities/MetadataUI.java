package com.tcvcog.tcvce.entities;

/**
 * Stores the label, key, and value of a single metadata property.
 * This object doesn't represent anything in the database, and it's only used
 * for viewing data.
 * @author Nathan Dietz
 */
public class MetadataUI {
    
    private String label;
    private String key;
    private String value;

    public MetadataUI(String label, String key, String value) {
        this.label = label;
        this.key = key;
        this.value = value;
    }
    
    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
    
}
