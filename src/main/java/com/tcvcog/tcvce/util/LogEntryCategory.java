package com.tcvcog.tcvce.util;

import com.tcvcog.tcvce.domain.ExceptionSeverityEnum;

/**
 *
 * @author sylvia
 */
public enum LogEntryCategory {
    EXCEPTION("ex", "Exception thrown", true),
    SESSINIT("init", "Session initialization", false);
    
    private final String title;
    private final String description;
    private boolean error;
    
    private LogEntryCategory(String t, String d, boolean er){
        title = t;
        description = d;
        error = er;
   }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @return the error
     */
    public boolean isError() {
        return error;
    }

    /**
     * @param error the error to set
     */
    public void setError(boolean error) {
        this.error = error;
    }
    
}
